#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t n)
{
	unsigned int	i;

	if (dst == NULL || src == NULL)
		return (0);
	if (n <= 0)
		return (ft_strlen(src));
	i = 0;
	while (i < (n - 1) && src[i] != '\0')
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (ft_strlen(src));
}

#include "libft.h"

int	ft_atoi(const char *str)
{
	int	s;
	int	r;

	s = 1;
	r = 0;
	if (*str != '\0')
	{
		while ((*str == '\t') || (*str == '\n') || (*str == '\v')
			|| (*str == '\f') || (*str == '\r') || (*str == ' '))
			str++;
		while (*str == '-' || *str == '+')
		{
			if (*str == '-')
				s *= (-1);
			str++;
			if (*str == '-' || *str == '+')
				return (0);
		}
		while (*str >= '0' && *str <= '9')
		{
			r = r * 10 + ((*str) - '0');
			str++;
		}
	}
	return (s * r);
}
